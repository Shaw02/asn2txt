include	lib_dos.inc


;****************************************************************
;*		コード						*
;****************************************************************
;---------------------------------------------------------------|
;		ファイルのオープン				|
;---------------------------------------------------------------|
;	●引数							|
;		cName	ファイルネームのポインタ		|
;		cMode		0 ･･･ Read			|
;				1 ･･･ Write			|
;				2 ･･･ Randam			|
;	●返り値						|
;		ax	ハンドル				|
;---------------------------------------------------------------|
.code
File_Open	proc	near	uses cx dx ds,
		strName	:far ptr byte,
		cMode	:byte

		lds	dx,strName
		mov	al,cMode

		mov	ah,3dh		;
		int	21h		;

		.if(carry?)			;割り当て失敗時に飛ぶ。
			invoke	File_Err	;
		.endif

		ret			;
File_Open	endp			;
;****************************************************************
	end
