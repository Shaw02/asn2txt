;---------------------------------------------------------------|
;		１６進数コード〜ASCII CODE(255)			|
;---------------------------------------------------------------|
;	処理							|
;		１６進コードを１０進のアスキーコードに変換	|
;		スペースで、右揃え				|
;	引数							|
;		iValue		変換したい数値			|
;		*strBuff	文字列を格納するアドレス	|
;	返り値							|
;		strBuff		変換後の文字列			|
;---------------------------------------------------------------|
.code
Byte_2_Str10_F		proc	near	uses	ax,
	iValue	:BYTE,
	strBuff	:FAR PTR BYTE

	mov	al, iValue
	cbw
	invoke	Word_2_Str10_F,	ax,strBuff,3

	ret
Byte_2_Str10_F		endp
;****************************************************************
	end
