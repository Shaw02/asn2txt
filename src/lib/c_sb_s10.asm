include	lib_dos.inc


;****************************************************************
;*		コード						*
;****************************************************************
;---------------------------------------------------------------|
;		１６進数コード〜ASCII CODE(255)	（符号付き）	|
;---------------------------------------------------------------|
;	処理							|
;		１６進コードを１０進のアスキーコードに変換	|
;	引数							|
;		iValue		変換したい数値			|
;		*strBuff	文字列を格納するアドレス	|
;	返り値							|
;		strBuff		変換後の文字列			|
;---------------------------------------------------------------|
.code
SByte_2_Str10	proc	near	uses	ax,
	iValue	:SBYTE,
	strBuff	:FAR PTR BYTE

	mov	al,iValue
	cbw
	invoke	SWord_2_Str10,	ax,strBuff

	ret
SByte_2_Str10	endp
;****************************************************************
	end
