include	lib_dos.inc


;****************************************************************
;*		コード						*
;****************************************************************
;---------------------------------------------------------------|
;		ASCII CODE→１６進コード(-127〜255)		|
;---------------------------------------------------------------|
;	引数							|
;		strValue	変換したい文字列先頭アドレス	|
;	返り値							|
;		AL		変換後				|
;		DS:SI		次のアドレス			|
;		carry flag	0:成功 / 1:失敗			|
;---------------------------------------------------------------|
.code
Str10_2_Byte	proc	near,
		strValue	:FAR PTR BYTE

	local	cTemp		:BYTE

	mov	cTemp, ah
	invoke	Str10_2_Word,	strValue
	mov	ah, cTemp

	ret				;
Str10_2_Byte	endp			;
;****************************************************************
	end
