include	lib_dos.inc


;****************************************************************
;*		コード						*
;****************************************************************
;---------------------------------------------------------------|
;		ファイルの作成					|
;---------------------------------------------------------------|
;	●引数							|
;		*strName	ファイルネームのポインタ	|
;		iAttr		ファイル属性			|
;	●返り値						|
;		ax		ハンドル			|
;---------------------------------------------------------------|
.code
File_Create	proc	near	uses cx dx ds,
		strName	:far ptr byte,
		iAttr	:word

		lds	dx,strName
		mov	cx,iAttr
		mov	ah,3ch		;
		int	21h		;

		.if(carry?)			;割り当て失敗時に飛ぶ。
			invoke	File_Err	;
		.endif

		ret			;
File_Create	endp			;
;****************************************************************
	end
